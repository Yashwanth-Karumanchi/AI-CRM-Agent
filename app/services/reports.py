from docx import Document
from docx.shared import Inches, Pt, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH
import io
from datetime import datetime, date, timedelta
from typing import List, Optional
from app.logger import get_logger

logger = get_logger(__name__)

# ── Design constants ───────────────────────────────────
TEAL = RGBColor(0x2D, 0x7A, 0x6B)
MUTED = RGBColor(0x5A, 0x55, 0x50)
LIGHT = RGBColor(0x9A, 0x90, 0x80)


def _style_heading(para, color=TEAL):
    """Apply brand color to heading"""
    for run in para.runs:
        run.font.color.rgb = color
        run.font.bold = True


def _add_table(
    doc,
    headers: list,
    rows: list,
    col_widths: list = None
):
    """
    Add a styled table. Handles empty runs gracefully.
    col_widths in inches, optional.
    """
    if not rows:
        doc.add_paragraph("No data available.")
        doc.add_paragraph()
        return None

    table = doc.add_table(rows=0, cols=len(headers))
    table.style = "Table Grid"

    # Header row
    hdr = table.add_row().cells
    for i, h in enumerate(headers):
        hdr[i].text = str(h)
        runs = hdr[i].paragraphs[0].runs
        if runs:
            runs[0].bold = True
            runs[0].font.color.rgb = TEAL

    # Data rows
    for row_data in rows:
        row = table.add_row().cells
        for i, val in enumerate(row_data):
            if i < len(row):
                row[i].text = str(val) if val is not None else ""

    doc.add_paragraph()
    return table


def _add_section(doc, title: str, content: str):
    """Add a styled heading + paragraph"""
    h = doc.add_heading(title, level=1)
    _style_heading(h)
    doc.add_paragraph(content)
    doc.add_paragraph()


def _doc_setup(doc) -> None:
    """Set standard page margins"""
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1.25)
        section.right_margin = Inches(1.25)


def _doc_title(doc, title: str, subtitle: str) -> None:
    """Add branded title and subtitle"""
    t = doc.add_heading(title, 0)
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _style_heading(t, TEAL)

    sub = doc.add_paragraph(subtitle)
    sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if sub.runs:
        sub.runs[0].italic = True
        sub.runs[0].font.color.rgb = LIGHT
    doc.add_paragraph()


def _doc_footer(doc) -> None:
    """Add standard branded footer"""
    doc.add_paragraph()
    footer = doc.add_paragraph(
        "Generated by ARIA AI CRM — "
        "Please verify all information before use."
    )
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if footer.runs:
        footer.runs[0].italic = True
        footer.runs[0].font.size = Pt(9)
        footer.runs[0].font.color.rgb = LIGHT


def _to_bytes(doc) -> bytes:
    """Save document to bytes"""
    buf = io.BytesIO()
    doc.save(buf)
    buf.seek(0)
    return buf.getvalue()


